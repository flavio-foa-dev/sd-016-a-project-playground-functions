function highestCount(array) {
  let arraySort = array.sort((a, b) => b - a);
  let lastItem = arraySort[0];
  let result = 1;
  for (let i = 1; i < arraySort.length; i += 1) {
    if (arraySort[i] === lastItem) {
      lastItem = arraySort[i];
      result += 1;
    }
  }
  return result;
}

